#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int	i;
	int	k;
	int	l;

	i = 0;
	k = 0;
	if (s2[k] == '\0')
		return ((char *)s1);
	while (s1[i] != '\0')
	{
		l = i;
		k = 0;
		while (s1[l] == s2[k] && s2[k] != '\0')
		{
			l++;
			k++;
		}
		if (s2[k] == '\0')
			return ((char *)s1 + i);
		i++;
	}
	return (NULL);
}
