#include "ft_printf.h"

void	ft_putnbr(long long n)
{
	unsigned long long i;

	i = n;
	if (n < 0)
	{
		i = -n;
		ft_putchar('-');
	}
	if (i >= 10)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
	else
		ft_putchar(i + '0');
}
