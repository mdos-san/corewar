#include "ft_printf.h"

void	ft_putstr(char const *s)
{
	int	i;

	i = 0;
	if (!s || !*s)
		return ;
	while (s[i])
	{
		ft_putchar(s[i]);
		i++;
	}
	if (!s)
		return ;
}
