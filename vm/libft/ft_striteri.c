#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int k;

	k = 0;
	if (!s)
		return ;
	if (s && f)
	{
		while (s[k])
		{
			f(k, &s[k]);
			k++;
		}
	}
}
