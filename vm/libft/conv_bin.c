#include "ft_printf.h"

int	conv_bin(char *tab)
{
	int res;

	res = 0;
	if (tab[0] == '1')
		res = 128;
	if (tab[1] == '1')
		res = res + 64;
	if (tab[2] == '1')
		res = res + 32;
	if (tab[3] == '1')
		res = res + 16;
	if (tab[4] == '1')
		res = res + 8;
	if (tab[5] == '1')
		res = res + 4;
	if (tab[6] == '1')
		res = res + 2;
	if (tab[7] == '1')
		res = res + 1;
	return (res);
}
