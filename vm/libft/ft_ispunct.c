#include "libft.h"

int	ft_ispunct(int c)
{
	if ((c >= 33 && c <= 39) || (c >= 40 && c <= 47) || (c <= 58 && c >= 64)
	|| (c >= 91 && c <= 96) || (c >= 123 && c <= 126))
		return ((unsigned char)1);
	else
		return ((unsigned char)0);
}
