#include "libft.h"

int	ft_isspace(int c)
{
	if (c == '\t' || c == 32 || c == '\n' || c == '\v' || c == '\f' ||
c == '\r')
	{
		return ((unsigned char)1);
	}
	else
		return ((unsigned char)0);
}
