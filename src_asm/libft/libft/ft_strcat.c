#include <string.h>

char	*ft_strcat(char *s1, const char *s2)
{
	int	i;
	int	i2;

	i = 0;
	i2 = 0;
	if (!s1 || !s2)
		return (NULL);
	while (s1[i] != '\0')
		i++;
	while (s2[i2] != '\0')
	{
		s1[i] = s2[i2];
		i++;
		i2++;
	}
	s1[i] = '\0';
	return (s1);
}
