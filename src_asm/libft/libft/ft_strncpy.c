#include <string.h>

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	size_t	i;

	i = 0;
	while (*(src + i) != '\0' && i < n)
	{
		*(dst + i) = *(src + i);
		i++;
	}
	if (i < n)
	{
		while (*(dst + i) != '\0' && i < n)
		{
			*(dst + i) = '\0';
			i++;
		}
	}
	return (dst);
}
